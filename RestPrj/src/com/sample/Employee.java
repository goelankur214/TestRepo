package com.sample;

public class Employee {

	private String empdob;
	private String empname;
	private String empplace;

	/**
	 * @return the empdob
	 */
	public String getEmpdob() {
		return empdob;
	}

	/**
	 * @return the empname
	 */
	public String getEmpname() {
		return empname;
	}

	/**
	 * @return the empplace
	 */
	public String getEmpplace() {
		return empplace;
	}

	/**
	 * @param empdob
	 *            the empdob to set
	 */
	public void setEmpdob(final String empdob) {
		this.empdob = empdob;
	}

	/**
	 * @param empname
	 *            the empname to set
	 */
	public void setEmpname(final String empname) {
		this.empname = empname;
	}

	/**
	 * @param empplace
	 *            the empplace to set
	 */
	public void setEmpplace(final String empplace) {
		this.empplace = empplace;
	}



}
